Inignoto* Inignoto::game;

void Inignoto::run() {
	game = this;
	initWindow();
	initVulkan();
	Translation::loadTranslations("Inignoto");
	Settings::loadSettings();
	loop();
	dispose();
}

void Inignoto::initWindow() {
	glfwInit();
	glfwWindowHint(GLFW_CLIENT_API, GLFW_NO_API);
	glfwWindowHint(GLFW_RESIZABLE, GLFW_TRUE);
	std::string str = "Inignoto ";
	str += Constants::version;
	Utils::window = glfwCreateWindow(Utils::FRAME_WIDTH, Utils::FRAME_HEIGHT, str.c_str(), nullptr, nullptr);
	
	glfwSetKeyCallback(Utils::window, Events::keyCallback);
	glfwSetCursorPosCallback(Utils::window, Events::mousePos);
	glfwSetMouseButtonCallback(Utils::window, Events::mouseClick);
	glfwSetWindowFocusCallback(Utils::window, Events::windowFocus);

	int width = 0;
	int height = 0;

	glfwGetWindowSize(Utils::window, &width, &height);
	const GLFWvidmode* vidmode = glfwGetVideoMode(glfwGetPrimaryMonitor());
	if (vidmode != nullptr) {
		glfwSetWindowPos(Utils::window,
			(vidmode->width - width) / 2,
			(vidmode->height - height) / 2);
	}
}

